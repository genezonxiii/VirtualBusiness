package tw.com.aber.vo;

public class LogisticVO {
	
	private String logisticId;
	private String groupId;
	private String userId;
	private String orderNo;
	private String sfWhFlag;
	private String shipmentId;
	private String waybillNo;
	private String ezcatFlag;
	private String trackingNumber;
	private String shippingCompany;
	private String returnLabel;
	public String getLogisticId() {
		return logisticId;
	}
	public void setLogisticId(String logisticId) {
		this.logisticId = logisticId;
	}
	public String getGroupId() {
		return groupId;
	}
	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}
	public String getSfWhFlag() {
		return sfWhFlag;
	}
	public void setSfWhFlag(String sfWhFlag) {
		this.sfWhFlag = sfWhFlag;
	}
	public String getShipmentId() {
		return shipmentId;
	}
	public void setShipmentId(String shipmentId) {
		this.shipmentId = shipmentId;
	}
	public String getWaybillNo() {
		return waybillNo;
	}
	public void setWaybillNo(String waybillNo) {
		this.waybillNo = waybillNo;
	}
	public String getEzcatFlag() {
		return ezcatFlag;
	}
	public void setEzcatFlag(String ezcatFlag) {
		this.ezcatFlag = ezcatFlag;
	}
	public String getTrackingNumber() {
		return trackingNumber;
	}
	public void setTrackingNumber(String trackingNumber) {
		this.trackingNumber = trackingNumber;
	}
	public String getShippingCompany() {
		return shippingCompany;
	}
	public void setShippingCompany(String shippingCompany) {
		this.shippingCompany = shippingCompany;
	}
	public String getReturnLabel() {
		return returnLabel;
	}
	public void setReturnLabel(String returnLabel) {
		this.returnLabel = returnLabel;
	}
	
}
